import React, { useState, useEffect, useRef } from 'react';

const Dropdown = ({ label, options, selected, onSelectedChange }) => {
  const [open, setOpen] = useState(false);
  const ref = useRef();

  useEffect(() => {
    const onBodyClick = (event) => {
        

      //chchecking if the ref component exists to avoid cleanup errors
      if (ref.current !== null && ref.current.contains(event.target)) {
        return; 
        //if we are clicking inside the dropdown component we don't need to setOpen false in here as we are handling it somewhere else
      };

      setOpen(false);
    };

    document.body.addEventListener('click', onBodyClick);

    return () => {
         document.body.removeEventListener('click', onBodyClick); 
         //in case the component unmounts we want to unmount the event listener too
    };
  }, []);

  const renderedOptions = options.map((option) => {
    if (option.value === selected.value) {
      return null;
    }

    return (
      <div
        key={option.value}
        className="item"
        onClick={() => onSelectedChange(option)}
      >
        {option.label}
      </div>
    );
  });

  return (
    <div ref={ref} className="ui form">
      <div className="field">
        <label className="label">{label}</label>
        <div
          onClick={() => setOpen(!open)}
          className={`ui selection dropdown ${open ? 'visible active' : ''}`}
        >
          <i className="dropdown icon"></i>
          <div className="text">{selected.label}</div>
          <div className={`menu ${open ? 'visible transition' : ''}`}>
            {renderedOptions}
          </div>
        </div>
      </div>
    </div>
  );
};

export default Dropdown;
